public class Assignment {

    public static void main(String[] args) {

     int marks =80;

     if(marks>75){

         System.out.println("FIRST DIVISION..");
     }

     marks=55;
     if(marks>50){

         System.out.println("SECOND DIVISION..");
     }

     marks=42;

     if(marks>40){

         System.out.println("THIRD DIVISION..");
     }

    }
}
